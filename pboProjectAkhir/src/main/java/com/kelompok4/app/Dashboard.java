    public void dashboard2_Load()
    {
        int c2;
        
        try {
            pst = con.prepareStatement("select * from laporankeuangan");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c2= rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable2.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i =1; i<=c2; i++)
                {
                    v2.add(rs.getString("id_laporan"));
                    v2.add(rs.getString("tanggal"));
                    v2.add(rs.getString("total_penjualan"));
                    v2.add(rs.getString("total_pembelian"));
                   
                }
                
                d.addRow(v2);
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void dashboard1_Load()
    {
        int c;
        
        try {
            pst = con.prepareStatement("select * from targetkeuangan");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i =1; i<=c; i++)
                {
                    v2.add(rs.getString("id_target"));
                    v2.add(rs.getString("target_pendapatan"));
                    v2.add(rs.getString("periode"));
                   
                }
                
                d.addRow(v2);
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

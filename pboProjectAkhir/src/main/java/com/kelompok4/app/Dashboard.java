    private void buttonTambah2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambah2ActionPerformed
        SimpleDateFormat date_format = new SimpleDateFormat("yyy-MM-dd");
        String tanggal = date_format.format(txttanggal.getDate());
        
        String penjualan = txtpenjualan.getText();
        String pembelian = txtpembelian.getText();
        
        
        
       
        try {
            pst = con.prepareStatement("insert into laporankeuangan (tanggal,total_penjualan,total_pembelian)values(?,?,?)");
            pst.setString(1, tanggal);
            pst.setString(2, penjualan);
            pst.setString(3, pembelian);
            
            
            int k = pst.executeUpdate();
            
            if(k== 1)
            {
                JOptionPane.showMessageDialog(this, "target keuangan Created");
                
                txtpenjualan.setText("");
                txtpembelian.setText("");
                
                dashboard2_Load();
                

            }
            else
            {
                JOptionPane.showMessageDialog(this,"Category  Error");
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonTambah2ActionPerformed

